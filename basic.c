/* Basic Operator Functions for function pointers */
#include "basic.h"

double add(double a, double b) {
    return (a + b);
}

double sub (double a, double b) {
    return (a - b);
}

double mul(double a, double b) {
    return (a * b);
}


double divd(double a, double b) {
    return (a / b);
}

double powr(double a, double b) {
    return(pow(a, b));
}

